package unidade5;
public class Calculadora {
    public static double somar(double n1, double n2) {
        return n1 + n2;
    }    
    public static double somar(double n1, int n2) {
        return n1 + n2;
    }    
    public static double somar(int n1, double n2) {
        return n1 + n2;
    }    
    public static double somar(int n1, int n2) {
        return n1 + n2;
    }
}
